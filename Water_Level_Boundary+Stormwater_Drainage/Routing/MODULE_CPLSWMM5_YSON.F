                        WRITE(S5UNITINP, '(A)') '[TIMESERIES]'
                        WRITE(S5UNITINP, '(A)') ';;Name           Date       Time       Value     '
                        WRITE(S5UNITINP, '(A)') ';;-------------- ---------- ---------- ----------'
