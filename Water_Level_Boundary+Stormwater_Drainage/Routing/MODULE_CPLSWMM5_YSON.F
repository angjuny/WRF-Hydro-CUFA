        IMPLICIT NONE
        INTEGER :: FLAGHSFUSE = 0, FLAGHSFSAVE = 0
        INTEGER :: ERR, I, J, KJ, KO
        CHARACTER(LEN = 150) :: LINE
        CHARACTER(LEN = 10) :: TCHAR10_1, TCHAR10_2, TCHAR10_3, TCHAR10_4!, TCHAR10_5
