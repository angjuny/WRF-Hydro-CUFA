						WRITE(S5UNITINP, '(A)') 'AVERAGES NO'
                        WRITE(S5UNITINP, '(A)') 'FLOWSTATS NO'
                        WRITE(S5UNITINP, '(A)') 'CONTROLS NO'
                        WRITE(S5UNITINP, '(A)') 'SUBCATCHMENTS NONE'
